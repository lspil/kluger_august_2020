package model;

public class Curs {

  private int id;
  private String name;
  private int durata;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getDurata() {
    return durata;
  }

  public void setDurata(int durata) {
    this.durata = durata;
  }
}
