package model;

public class Clasa {

  private int id;
  private String nume;
  private Curs curs;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getNume() {
    return nume;
  }

  public void setNume(String nume) {
    this.nume = nume;
  }

  public Curs getCurs() {
    return curs;
  }

  public void setCurs(Curs curs) {
    this.curs = curs;
  }
}
